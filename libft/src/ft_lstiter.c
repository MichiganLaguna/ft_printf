#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*tmp;

	if (lst != 0)
	{
		tmp = lst->next;
		f(lst->content);
		ft_lstiter(tmp, f);
	}
}
