#include "printf.h"

int	ft_printnbr(int nbr)
{
	int	i;

	ft_putnbr_fd(nbr, 1);
	i = 1;
	while (nbr / 10 > 0)
	{
		i++;
		nbr /= 10;
	}
	return (i);
}
